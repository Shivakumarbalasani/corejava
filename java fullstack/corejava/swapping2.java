class Swapping2
{
	public static void main(String args[])
	{
		int num1=5;
		int num2=30;
			System.out.println("Before Swapping");
			System.out.println(num1);
			System.out.println(num2);
		num1=num1*num2;//150
		num2=num1/num2;//5
		num1=num1/num2;//30
			System.out.println("After swapping");
			System.out.println(num1);
			System.out.println(num2);
		
			
		
			

	}


}