class Swapping
{

	public static void main(String args[])
	{
	int num1=20;
	int num2=30;
	
        System.out.println("Before Swapping");
	System.out.println(num1);
	System.out.println(num2);

	int temp=num2;
        num2=num1;
	num1=temp;

	System.out.println("After Swapping");
	System.out.println(num1);
	System.out.println(num2);
	
	

	}
}